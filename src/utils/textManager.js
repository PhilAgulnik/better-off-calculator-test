// Load text blocks from localStorage or use defaults
export const getTextBlocks = async () => {
  try {
    const stored = localStorage.getItem('calculatorTextBlocks');
    if (stored) {
      return JSON.parse(stored);
    } else {
      // Initialize with default text blocks
      localStorage.setItem('calculatorTextBlocks', JSON.stringify(defaultTextBlocks));
      return defaultTextBlocks;
    }
  } catch (error) {
    console.error('Error loading text blocks:', error);
    return defaultTextBlocks;
  }
};

// Update a text block
export const updateTextBlock = async (key, newValue, editorName) => {
  try {
    const blocks = await getTextBlocks();
    const updatedBlocks = blocks.map(block => 
      block.key === key 
        ? { 
            ...block, 
            english: newValue, 
            editorName, 
            editHistory: 'Modified',
            lastModified: new Date().toISOString()
          }
        : block
    );
    localStorage.setItem('calculatorTextBlocks', JSON.stringify(updatedBlocks));
    return true;
  } catch (error) {
    console.error('Error updating text block:', error);
    throw error;
  }
};

// Delete a text block
export const deleteTextBlock = async (key) => {
  try {
    const blocks = await getTextBlocks();
    const updatedBlocks = blocks.filter(block => block.key !== key);
    localStorage.setItem('calculatorTextBlocks', JSON.stringify(updatedBlocks));
    return true;
  } catch (error) {
    console.error('Error deleting text block:', error);
    throw error;
  }
};

// Get a specific text block by key
export const getTextBlock = async (key) => {
  try {
    const blocks = await getTextBlocks();
    return blocks.find(block => block.key === key);
  } catch (error) {
    console.error('Error getting text block:', error);
    return null;
  }
};

// Get text value by key (for use in components)
export const getText = async (key, defaultValue = '') => {
  try {
    const block = await getTextBlock(key);
    return block ? block.english : defaultValue;
  } catch (error) {
    console.error('Error getting text:', error);
    return defaultValue;
  }
};

// Reset all text blocks to defaults
export const resetTextBlocks = async () => {
  try {
    localStorage.setItem('calculatorTextBlocks', JSON.stringify(defaultTextBlocks));
    return true;
  } catch (error) {
    console.error('Error resetting text blocks:', error);
    throw error;
  }
};
